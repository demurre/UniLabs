SELECT Район.[Код района], Район.Район, AVG(CAST(Приміщення.[Ціна за 1 кв м] AS DECIMAL)) AS [Середня ціна оренди] 
FROM Приміщення
JOIN Район ON Приміщення.[Код района] = Район.[Код района]
GROUP BY Район.[Код района], Район.Район;
